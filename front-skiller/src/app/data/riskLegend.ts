/**
 * Clas representing a legend of a risk.
 */
export class RiskLegend {

  /**
   * level of risk (from 0 to 10).
   */
  public level: number;

  /**
   * color associated to the color.
   */
  public color: string;

  /**
   * Explanation for this level of risk.
   */
  public description: string;

}
